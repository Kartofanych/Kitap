package com.example.irekonelove4.Models;

public class User {
    public String name;
    public String about;
    public int id;

    public User() {
        this.name = name;
        this.about = about;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAbout() {
        return about;
    }

    public void setAbout(String about) {
        this.about = about;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}